            <p class="info-value">已使用：{{ systemInfo.size }}</p>
          </flexbox-item>
        </flexbox>
      </div>

      <!-- 基本信息 -->
      <div class="section">
        <div class="section-title">企业基本信息设置</div>
        <div class="section-content">
          <div class="name">企业名称</div>
          <el-input
            v-model="systemName"
            :maxlength="50"/>
        </div>
        <div class="section-content">
          <div class="name">企业logo</div>
          <el-upload
            v-if="!systemImage"
            :show-file-list="false"
            :http-request="fileUpload"
            drag
            class="upload"
            action="http"
            accept="image/png, image/jpeg, image/gif, image/jpg">
            <i class="el-icon-plus uploader-icon"/>
          </el-upload>
          <div
            v-else
            class="upload-show">
            <img v-src="systemImage">
            <i
              class="el-icon-remove icon-delete"
              @click="deleteSystemImage"/>
          </div>
        </div>
      </div>

      <el-button
        v-if="systemSaveAuth"
        class="save-button"
        type="primary"
        @click="save">保存</el-button>
    </div>
    <edit-image
      :fixed-number="[15, 4]"
      :show="showEditImage"
      :image="editImage"
      :file="editFile"
      title="编辑企业logo"
      preview-width="150px"
      preview-height="40px"
      preview-radius="0"
      width="550px"
      save-button-title="确定"
      @save="submiteImage"
      @close="showEditImage=false"/>
  </flexbox>
</template>

<script>
import { adminSystemSave, adminSystemConfigIndex } from '@/api/systemManagement/SystemConfig'

import RadialProgressBar from 'vue-radial-progress'
import EditImage from '@/components/EditImage'
import XrHeader from '@/components/xr-header'

import { mapGetters } from 'vuex'
import { fileSize } from '@/utils/index'
import moment from 'moment'

export default {
  name: 'SystemConfig',
  components: {
    EditImage,
    XrHeader,
    RadialProgressBar
  },
  data() {
    return {
      loading: false,
      showEditImage: false,
      editImage: null,
      editFile: null,
      systemName: '',
      systemImage: '',
      editedImage: null, // 编辑后的图片
      systemInfo: null // 系统信息
    }
  },
  computed: {
    ...mapGetters(['manage']),
    systemSaveAuth() {
      return this.manage && this.manage.system && this.manage.system.update
    }
  },

  created() {
    this.getDetail()
    this.getSystemInfo()
  },
  methods: {
    /** 附件上传 */
    fileUpload(content) {
      const reader = new FileReader()
      var self = this
      reader.onload = function(e) {
        let result
        if (typeof e.target.result === 'object') {
          // 把Array Buffer转化为blob 如果是base64不需要
          result = window.URL.createObjectURL(new Blob([e.target.result]))
        } else {
          result = e.target.result
        }
        self.editImage = result
        self.editFile = content.file
        self.showEditImage = true
      }
      reader.readAsDataURL(content.file)
    },
    deleteSystemImage() {
      this.systemImage = ''
      this.editedImage = null
    },
    getDetail() {
      this.loading = true
      this.$store
        .dispatch('SystemLogoAndName')
        .then(res => {
          this.loading = false
          this.systemName = res.data.name ? res.data.name : ''
          this.systemImage = res.data.logo
        })
        .catch(() => {
          this.loading = false
        })
    },

    /**
     * 获取系统使用信息
     */
    getSystemInfo() {
      this.loading = true
      adminSystemConfigIndex().then(res => {
        const data = res.data || {}
        const startMoment = moment(data.startTime)
        const endMoment = moment(data.endTime)

        // data.startTime = startMoment.format('YYYY-MM-DD')
        // data.endTime = endMoment.format('YYYY-MM-DD')
        data.surplusDays = endMoment.diff(moment(), 'days')
        const totalDays = endMoment.diff(startMoment, 'days')
        data.dayProgress = data.surplusDays > 0 ? Math.floor((data.surplusDays / totalDays) * 100) : 0

        data.sizeProgress = data.size > 0 ? (data.size / data.allSize) * 100 : 100
        data.size = fileSize(data.size)
        data.allSize = fileSize(data.allSize)

        this.systemInfo = data
        this.loading = false
      }).catch(() => {
        this.loading = false
      })
    },

    submiteImage(data) {
      this.editedImage = data
      this.systemImage = data.image
    },
    save() {
      if (!this.systemName) {
        this.$message.error('企业名称不能为空')
      } else {
        this.loading = true
        var param = new FormData()
        param.append('name', this.systemName)
        // 编辑头像了
        if (this.editedImage) {
          param.append(
            'file',
            this.editedImage.blob,
            this.editedImage.file.name
          )
        } else {
          // 头像删除时 传
          if (!this.systemImage) {
            param.append('company_logo', '')
          }
        }
        adminSystemSave(param)
          .then(res => {
            this.loading = false
            this.$message.success('操作成功')
            this.getDetail()
          })
          .catch(() => {
            this.loading = false
          })
      }
    }
  }
}
</script>

